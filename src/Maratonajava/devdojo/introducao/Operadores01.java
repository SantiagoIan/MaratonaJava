package Maratonajava.devdojo.introducao;


public class Operadores01 {
    public static void main(String[] args) {

        int num = 10;
        int num2 = 2;

        int div = num / num2;
        int multi = num * num2;

        System.out.println(++num); // 10 + 1
        System.out.println(--num2); // 2 - 1
        System.out.println(div); // 10 / 2
        System.out.println(multi); // 10 * 2


    }
}
